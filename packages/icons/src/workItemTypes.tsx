export type WorkItemTypeIconProps = {
  /** The work item type to render. */
  name: WorkItemType;
  /** Pixel size for both width and height. @default 16 */
  size?: number;
  /**
   * Render a trailing label next to the icon ("Epic", "QA Bug", …).
   * `false` (default) → icon-only. `true` → uses the canonical label
   * from `WORK_ITEM_TYPE_LABELS`. Pass a custom string to override.
   */
  previewTitle?: boolean | string;
  /** Accessible label override. Defaults to the canonical title. */
  "aria-label"?: string;
  className?: string;
  style?: CSSProperties;
};

/**
 * Renders the brand-colored glyph for a work-item type. By default the
 * icon is decorative (`aria-hidden`) and consumers handle the label
 * separately. Set `previewTitle` to render the label inline.
 */
export function WorkItemTypeIcon({
  name,
  size = 16,
  previewTitle = false,
  "aria-label": ariaLabel,
  className,
  style,
}: WorkItemTypeIconProps): JSX.Element {
  const Glyph = workItemTypeRegistry[name];
  const label =
    typeof previewTitle === "string"
      ? previewTitle
      : WORK_ITEM_TYPE_LABELS[name];
  const showLabel = previewTitle !== false && previewTitle !== undefined;

  if (!showLabel) {
    return (
      <span
        role="img"
        aria-label={ariaLabel ?? label}
        className={className}
        style={{ display: "inline-flex", alignItems: "center", ...style }}
      >
        <Glyph size={size} />
      </span>
    );
  }
  return (
    <span
      className={className}
      style={{
        display: "inline-flex",
        alignItems: "center",
        gap: "6px",
        ...style,
      }}
    >
      <Glyph size={size} aria-hidden="true" />
      <span>{label}</span>
    </span>
  );
}
