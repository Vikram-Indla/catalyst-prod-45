/**
 * TestHub QA Assistant — Edge Function Template
 * Powered by Lovable AI Gateway (Gemini 3 Flash)
 * 
 * This is a blank template ready for QA-specific logic.
 * Currently provides a generic QA assistant response.
 */

import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

const SYSTEM_PROMPT = `You are CATY AI™ — QA Assistant, a specialized AI assistant for the TestHub quality assurance module. You help QA engineers with:

- Test case creation and optimization
- Test cycle planning and execution guidance
- Defect triage and root cause analysis
- Test coverage analysis and gap identification
- QA best practices and methodology recommendations
- Test data management suggestions
- Regression testing strategies

Keep your answers clear, concise, and actionable. Format responses with proper structure using HTML.
When providing lists or structured data, use proper HTML formatting with divs and classes.

IMPORTANT: You are a QA-focused assistant. Do not answer questions outside the QA/testing domain.
Politely redirect off-topic questions back to QA topics.`;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages, context } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    // Build context-aware system prompt
    let contextPrompt = SYSTEM_PROMPT;
    if (context) {
      contextPrompt += `\n\nCurrent context:`;
      if (context.module) contextPrompt += `\n- Module: ${context.module}`;
      if (context.page) contextPrompt += `\n- Current page: ${context.page}`;
    }

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: contextPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded, please try again later." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "Usage limit reached. Please add credits." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI gateway error" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("QA AI error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
